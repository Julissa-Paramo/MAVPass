# dictionaries
# values in dictionaries
# for loops

pop_dict = {'New York':100,'Minnesota':200,'Mississippi':300,'Vermont':400}

pop_sum = 0 # counter for the sum of values

for value in pop_dict.values(): # gets the values from the dictionary
  
  pop_sum += value # adds each value to the sum
  
print(pop_sum) # result is 1000 for the total population


  
